#Crie uma função que receba dois números e retorne o resultado da multiplicação.

def multiplicacao_algoritmos(a, b):
    return a * b

primeiro_algoritmo = int(input('Digite o primeiro algoritimo: '))
segundo_algoritmo = int(input('Digite o segundo algoritimo: '))
