# Exercício 15: Calcular área de um retângulo
# A fórmula da área é largura * altura.
largura = float(input("Digite a largura do retângulo: "))
altura = float(input("Digite a altura do retângulo: "))

# Calculamos a área.
area = largura * altura

# Exibimos o resultado.
print(f"A área de um retângulo com largura {largura} e altura {altura} é {area}.")

