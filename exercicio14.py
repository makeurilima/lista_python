# Exercício 14: Converter metros para centímetros
# 1 metro é igual a 100 centímetros.
valor_metros = float(input("Digite um valor em metros: "))

# Realizamos a conversão.
valor_centimetros = valor_metros * 100

# Exibimos o resultado da conversão.
print(f"{valor_metros} metro(s) equivale(m) a {valor_centimetros} centímetros.")

