# Exercício 12: Dividir dois números
# Usamos float() para converter os inputs, pois a divisão pode resultar em um número decimal.
dividendo = float(input("Digite o primeiro número (dividendo): "))
divisor = float(input("Digite o segundo número (divisor): "))

# Realizamos a divisão.
resultado_divisao = dividendo / divisor

# Exibimos o resultado.
print(f"A divisão de {dividendo} por {divisor} é igual a {resultado_divisao}.")

