# Exercício 11: Multiplicar número por 5
# Pedimos um número e o convertemos para inteiro.
numero_para_multiplicar = int(input("Digite um número inteiro: "))

# Calculamos o resultado da multiplicação.
resultado_multiplicacao = numero_para_multiplicar * 5

# Mostramos o resultado.
print(f"O resultado de {numero_para_multiplicar} multiplicado por 5 é {resultado_multiplicacao}.")

