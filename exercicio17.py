# Exercício 17: Calcular o dobro e o triplo
# Pedimos um número ao usuário. Usamos float para aceitar também números decimais.
numero_base = float(input("Digite um número: "))

# Calculamos o dobro.
dobro = numero_base * 2
# Calculamos o triplo.
triplo = numero_base * 3

# Exibimos os resultados em mensagens separadas.
print(f"O dobro de {numero_base} é {dobro}.")
print(f"O triplo de {numero_base} é {triplo}.")